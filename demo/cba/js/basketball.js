    ,draw:function(polys, pt){
        var me = this;
        var width = me.width;
        var height = me.height;
        var ctx = me.ctx;
        var datas = me._data || [];
        var textPoints = me.textPoints || [];

        ctx.clearRect(0, 0, width, height); 

        $.each(polys, function(index, poly){
            var cnt = 0;
            var data = datas[index] || {};
            ctx.beginPath();
            $.each(poly, function(key, item){
                if(cnt == 0){
                    ctx.moveTo(item.x, item.y);
                }
                ctx.lineTo(item.x, item.y);
                cnt++;
            });

            ctx.fillStyle = me.getBackgroundColor(data, pt, poly);
            ctx.closePath();
            ctx.fill();
        }); 

        me.img && ctx.drawImage(me.img, 0, 0, width, height);

        $.each(polys, function(index, poly){
            var data = datas[index] || {};
            var textPoint = textPoints[index];
            me.setText(data, textPoint, pt, poly);
        }); 
    }
    ,isNumber:function(value){
        return /^\d+(\.\d)*$/.test(value);
    }
    ,setText:function(item, textPoint, pt, poly){
        item = item || {};
        textPoint = textPoint || {};
        var me = this;

        if(!item || (!me.isNumber(item.all) || !me.isNumber(item.score))){
            return;
        }

        var ctx = me.ctx;
        var angle = me.angle;
        var opt = me._opt || {};
        var all = item.all;
        var score = item.score;
        var x1 = textPoint.x1;
        var y1 = textPoint.y1;
        var x2 = textPoint.x2;
        var y2 = textPoint.y2;
        var size1 = textPoint.size1 || opt.fontSize;
        var size2 = textPoint.size2 || opt.fontSize;
        var width = me.width;
        var height = me.height;
        var percent = (score * 100 / all).toFixed(1) + "%";
        var scale = score + "/" + all;
        var fontStyle1 = "normal normal bold " + size1 + "px arial";
        var fontStyle2 = "normal normal normal " + size2 + "px arial";
        ctx.fillStyle = me.getFontColor(item, pt, poly);

        var offset = size1 * 0.7;
        ctx.font = fontStyle1
        ctx.fillText(percent, x1, y1 + offset);
        ctx.font = fontStyle2;
        ctx.fillText(scale, x2, y2 + offset);
    }
    ,getFontColor:function(item, pt, poly){
        var me = this;
        var opt = me._opt || {};
        var isHover = me.isPointInPolys(pt, poly);
        var color = item.fontColor || opt.fontColor;
        var hoverColor = item.hoverFontColor || opt.hoverFontColor;
        if(isHover){
            return hoverColor;
        }else{
            return color;
        }
    }
    ,getBackgroundColor:function(item, pt, poly){
        var me = this;
        var opt = me._opt || {};
        var isHover = me.isPointInPolys(pt, poly);
        var color = item.color || opt.defaultColor;
        var hoverColor = item.hoverColor || opt.hoverColor;
        if(isHover){
            return hoverColor;
        }else{
            return color;
        }
    }
    ,onmousemove:function(polys, e){
        var me = this;
        var xy = {
            x:e.pageX,
            y:e.pageY
        };
        var offset = $(me.cnvs).offset() || {};
        var offsetX = offset.left;
        var offsetY = offset.top;
        me.draw(polys, {
            x:xy.x - offsetX,
            y:xy.y - offsetY
        });
    }
    ,isPointInPolys:function(pt, poly){
        if(pt == null){
            return false;
        }
        var iPoly = null;
        var jPoly = null;
        for (var c = false, i = -1, l = poly.length, j = l - 1; ++i < l; j = i) {
            iPoly = poly[i];
            jPoly = poly[j];
            ((iPoly.y <= pt.y && pt.y < jPoly.y) || (jPoly.y <= pt.y && pt.y < iPoly.y)) 
            && (pt.x < (jPoly.x - iPoly.x) * (pt.y - iPoly.y) / (jPoly.y - iPoly.y) + iPoly.x) 
            && (c = !c); 
        }
        return c; 
    }
