
/** 篮球场画布
 *
 *  @constructs Basketball
 *  @date 2018.02.08
 *  @param {Object} opts                                [*]参数
 *  @param {String|HTMLElement} opts.node               [*]Basketball的容器
 *  @param {Number} opts.angle                          旋转角度，默认旋转0度；仅支持0、90、180、270
 *  @param {String} opts.imgRootPath                    图片的相对根
 *  @param {Object} opts.data                           文本内容数据
 *  @param {Function} opts.fail                         图片加载失败, 参数为失败的信息
 *  @param {Function} opts.inited                       Basketball处理完成后的回调函数
 *  @return {Basketball} Basketball的实例
 *  @version 1.0.0
 *  @example
        new Basketball({
            // Basketball节点Dom容器
            node:"#canvas1",
            // 角度图片的相对根路径
            imgRootPath:"",
            // 角度，仅支持0、90、180、270
            angle:0,
            // 每块区域(共十块区域)的文本内容、默认色、hover色
            data:[{
                all:57,                 // 总分
                score:21,               // 当前得分
                hoverColor:"#aaa",      // 区域块hover背景颜色,      默认red
                color:"#d1dae4",        // 区域块默认背景颜色,        默认#e1c199
                fontColor:"#222",       // 区域块的文本默认字体颜色,   默认"#222"
                hoverFontColor:"#222"   // 区域块的文本hover字体颜色, 默认"#222"
            }],
            // 当前角度图片被加载失败时回调
            fail:function(msg){
                console.log(msg)
            },
            // Basketball控件初始化完成时回调
            inited:function(){
                console.log("初始化完成");
            }
        });
 */
function Basketball(opt){
    opt = opt || {};

    var me = this;
    var node = opt.node;
    var inited = opt.inited;
    var angle = opt.angle || 0;
    var imgRootPath = $.trim(opt.imgRootPath);
    var data = opt.data;
    var element = $(node).get(0);

    if(element == null){
        throw new Error("Container[" + node + "] does not exist");
    }
    
    if($.inArray(angle, [0, 90, 180, 270]) == -1){
        throw new Error("opt.angle support: 0, 90, 180, 270");
    }

    me.opt = opt || {};
    me.cnvs = element;
    me.ctx = element.getContext("2d");
    me.width = null;
    me.height = null;
    me.img = null;
    me.polys = null;
    me.textPoints = null;
    me.angle = angle;
    me.imgRootPath = imgRootPath;
    me._data = data;
    me._opt = {
        fontSize:14,
        fontColor:"#222",
        hoverFontColor:"#222",
        defaultColor:"#e1c199",
        hoverColor:"red"
    }

    me.init(function(){
        inited && inited.call(me);
    });
}

